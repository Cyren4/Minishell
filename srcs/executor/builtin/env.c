/*
	Prompt l'environnement
*/

void	ft_env(t_gen *data)
{
	t_env	*tmp;

	tmp = data->env;
	while (tmp != NULL)
	{
		printf("%s=%s\n", tmp->name, tmp->content);
		tmp = tmp->next;
	}
}
